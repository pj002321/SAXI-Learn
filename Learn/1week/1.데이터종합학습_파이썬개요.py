# [11. AI 에이전트 & LLM]
# - LLM (Large Language Model) : 대규모 언어 모델 (예: Claude)
# - Agent : 특정 역할을 부여받아 자율적으로 작업을 수행하는 AI
#   - 페르소나 : 에이전트에게 부여하는 역할/성격 (Markdown으로 정의)
# - 오케스트레이션 : 여러 Agent를 조율하고 관리하는 구조
#   - Harness(하네스) : Agent 실행 환경 전체를 감싸는 제어 프레임워크
#     - Agent가 어떤 도구를 쓸 수 있는지 허용/차단
#     - Agent의 입출력을 가로채 검사하거나 로깅
#     - 실행 순서, 타임아웃, 재시도 등 흐름 제어
#     - 쉽게 말해 : Agent가 날뛰지 못하도록 묶어두는 안전장치
#   - Guardrails(가드레일) : 하네스 안에서 동작하는 규칙 집합
#     - 입력 가드 : 위험한 입력이 Agent에 들어가기 전에 차단
#     - 출력 가드 : Agent가 생성한 결과가 기준을 벗어나면 필터링
#     - 예) 욕설 차단, 개인정보 마스킹, 환각(hallucination) 감지
# - 집단지성 : 경량화된 LLM 여러 개를 각 특성에 맞게 전문화해서 협력시키는 방식 (예: Sakana AI)

