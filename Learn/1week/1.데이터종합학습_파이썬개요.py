# [0. 변수 (Variable)]
# - 데이터를 저장하는 공간 (실제 저장 위치는 RAM)
# - 형식 : 변수명 = 값
# - 한 번에 한 개의 값만 보관 (재할당 시 이전 값은 사라짐)
# - 변수명 규칙
#   - 영문자, 숫자, 언더스코어(_) 사용 가능
#   - 숫자로 시작 불가 (예: 1name X)
#   - 대소문자 구분 (name 과 Name 은 다른 변수)
#   - 예약어 사용 불가 (if, for, while, def 등)
# - 자료형 (type)
#   일반적인 크기 (C/Java 기준, 컴퓨터 과학 개념)
#   - int   : 4 byte  (정수, 예: 10)
#   - float : 4 byte  (실수 단정도), 8 byte (배정도 double)
#   - char  : 1 byte  (문자 1개)
#   - bool  : 1 byte  (True / False)
#
#   Python 실제 크기 (객체 오버헤드 포함, sys.getsizeof 기준)
#   - int   : 28 byte  (숫자가 커질수록 늘어남)
#   - float : 24 byte
#   - str   : 49 byte~ (글자 수에 따라 증가, 한 글자당 +1~4 byte)
#   - bool  : 28 byte  (Python에서 bool은 int의 하위 타입)
#
#   Python이 더 큰 이유 : 모든 값이 '객체'이기 때문에 타입 정보, 참조 카운터 등이 추가됨

import sys
a = 10
b = 3.14
c = "hello"
d = True
print(sys.getsizeof(a))  # 28
print(sys.getsizeof(b))  # 24
print(sys.getsizeof(c))  # 54 (49 + 글자 수)
print(sys.getsizeof(d))  # 28

a = 10

print(*range(a))

if a < 10:
    print("a는 10보다 작습니다.")
elif a == 10:
    print("a는 10입니다.")
else:
    print("a는 10보다 크거나 같습니다.")

# [1. Python 기초 자료구조]
# - 리스트     : 순서 O, 수정/삭제 O, 중복 O  — 가장 범용적인 자료구조
# - 튜플       : 순서 O, 수정/삭제 X, 중복 O  — 변경 불가, 안전한 데이터 보관
# - 딕셔너리   : 순서 X, 수정/삭제 O, 키중복 X — 키-값 쌍으로 저장
# - set        : 순서 X, 수정/삭제 O, 중복 X  — 중복 제거가 필요할 때 사용

# 리스트
lst = [1, 2, 3, 2]
lst.append(4)       # 추가
lst.remove(2)       # 첫 번째 2 삭제
print(lst)          # [1, 3, 2, 4]

# 튜플
tpl = (1, 2, 3)
print(tpl[0])       # 1 (조회만 가능, 수정 불가)

# 딕셔너리
dct = {"name": "철수", "age": 20}
dct["age"] = 21     # 수정
print(dct["name"])  # 철수

# set
st = {1, 2, 2, 3}
print(st)           # {1, 2, 3} (중복 자동 제거)

